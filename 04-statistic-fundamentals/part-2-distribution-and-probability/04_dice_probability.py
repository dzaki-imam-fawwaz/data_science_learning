"""
TOPIC: Dice Probability

QUESTION:
What is the probability of rolling a specific number?

GOAL:
Understand simple probability using a six-sided dice.

FORMULA:
Probability = favorable outcomes / total outcomes
"""

total_outcomes = 6
favorable_outcomes = 1

probability_of_six = favorable_outcomes / total_outcomes

print("Total Outcomes:", total_outcomes)
print("Favorable Outcomes:", favorable_outcomes)
print("Probability of rolling a 6:", probability_of_six)

